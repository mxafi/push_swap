char	*ft_strrchr(const char *s, int c)
{
	char	*p;

	p = (char *)s;
	while (*p)
		p++;
	while (p != s && *p != (char)c)
		p--;
	if (*p != (char)c && p == s)
		return (0);
	return (p);
}
